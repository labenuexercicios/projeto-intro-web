const valorInput = document.getElementById('leitura')
const busca = document.getElementsByTagName('h2')
const cardsN = document.querySelectorAll('.card')





let nomesCard = []


for (let i in busca){
    nomesCard.push(busca[i].innerText)
}
  let nomesCards2 = nomesCard.filter((itens) => 
     itens!== undefined
  )


 nomesCards2 = nomesCards2.map((itens) =>
     itens.toLowerCase()
 )

const pesquisar = (event) => {
    
    const textoDePesquisa = valorInput.value.toLowerCase()
    event.preventDefault()
    cardsN.forEach((itens) =>{
        itens.classList.add("sumir")
        itens.classList.remove("centralizar")
     
    })

    for (let i in cardsN){
      if (nomesCards2[i] === textoDePesquisa){
         cardsN[i].classList.remove("sumir")
         cardsN[i].classList.add("centralizar")
      }
    }
    const mensagem = nomesCards2.includes(textoDePesquisa)
    const limpar = () =>{
        if (textoDePesquisa === ""){
            cardsN.forEach((itens) =>
                itens.classList.remove("sumir")
            )
        } else if (mensagem === false){
            alert ("Personagem não encontrado!")
         }
    } 
    limpar()
}






